	<h1>Personvern­erklæring</h1>

	<p>Her kan du lese om hvordan Klynge Næringsforening samler inn og bruker personopplysninger.</p>

	<h2>Dine rettigheter</h2>
	<p>
		Alle har rett på grunnleggende informasjon om behandlinger av personopplysninger i en virksomhet
		etter personopplysningslovens § 18, 1. ledd. Klynge Næringsforening utgir informasjon om dette i
		denne erklæringen. Alle som er registrert i våre systemer har rett på innsyn i egne
		opplysninger. Samt rett til å be om at uriktige, ufullstendige eller opplysninger foreningen
		ikke har adgang til å behandle blir rettet, slettet eller supplert.
	</p>
	<p>Krav fra den registrerte skal besvares kostnadsfritt og senest innen 30 dager.</p>

	<h2>Medlemsregisteret</h2>
	<p>
		Alle medlemmer av foreningen må registrere personopplysninger, herunder fornavn, etternavn,
		e-post, telefonnummer, adresse, arbeidssted og -stilling. Dersom medlemmer ønsker det, legges
		fullt navn, e-post, telefonnummer, eventuelt bilde og opplysninger om arbeidssted og -stilling
		på Klynge Næringsforenings nettside.
	</p>

	<p>
		Opplysningene lagres hos Sanity AS (og deres underleverandører) og av den grunn vår
		databehandler. Les selskapets personvvernerklæring her:
		<a href="https://www.sanity.io/legal/privacy">https://www.sanity.io/legal/privacy</a>
	</p>

	<p>
		Kildekode over vårt medlemsregister utgis under åpen kildekode, og kan ses her:
		<a href="https://github.com/klyngeorg/studio">https://github.com/klyngeorg/studio</a>
	</p>

	<h2>Påmelding til våre arrangementer</h2>

	<p>
		Når du melder deg på et av våre arrangementer vil opplysninger som fornavn, etternavn, e-post,
		telefonnummer, arbeidssted og andre nødvendige opplysninger lagres hos vår databehandler Google
		eller/og arrangementsystem. Opplysningene lagres kun midlertidig og kan deles med våre
		samarbeidspartnere, slik som restauranter, arrangører og utleiere. Alle opplysninger som deles
		videre anonymiseres så mye som mulig.
	</p>

	<p>
		I forkant og etterkant av arrangementet kan opplysningene også brukes til å kommunisere til de
		påmeldte via e-post og telefon, avgrenset til dette arrangementet. Vi sletter opplysningene
		senest etter 60 dager etter arrangementet er gjennomført eller avlyst.
	</p>

	<h2>Folk i Klynge</h2>

	<p>
		Alle som er interessert i Klynge, som ønsker å holde seg oppdatert og engasjert i foreningen,
		både medlemmer og tidligere gjester kan registrere seg i <em>Folk i Klynge</em>. Vi sender
		e-post ut til registrerte i Folk i Klynge ca. 2-3 ganger hver måned.
	</p>

	<p>
		Avregistrering gjøres ved å sende e-post til
		<a href="mailto:folk+unsubscribe@klyngeorg.no">folk+unsubscribe@klyngeorg.no</a>
		eller ved henvendelse hos personvernombudet. Ved registrering vil e-postadresse lagres hos vår databehandler
		Google.
	</p>

	<h2>Github og Google</h2>

	<p>
		I forbindelse med drift av foreningens e-post og kontorverktøy benyttes Google Workspace.
		Styrets medlemmer og andre engasjert i foreningen vil ha adgang til egne kontoer (herunder blant
		annet e-post).
	</p>

	<p>
		E-post sammen med kontaktopplysninger på mottatte og sendte e-poster kan lagres opptil tre år.
		E-post vil automatisk slettes ved avslutting av kontoer eller etter tre år.
	</p>

	<p>
		Vi benytter også Github for å lagre kode og dokumentasjon. Github er en tjeneste som tilbyr
		git-lagring i skyen. Github er eid av Microsoft. Les mer om Githubs personvernerklæring her:
		<a href="https://docs.github.com/en/github/site-policy/github-privacy-statement">
			https://docs.github.com/en/github/site-policy/github-privacy-statement
		</a>
	</p>

	<p>
		Vi sletter varsler fra forskjellige systemer sendt til e-post, som kan inneholde
		personopplysninger, automatisk. Dette gjør vi gjennom programvare fra Digital Inspiration. Deres
		personvernerklæring kan leses her:
		<a href="https://www.labnol.org/privacy">https://www.labnol.org/privacy</a>
	</p>

	<h2>Checkin</h2>

	<p>
		Vi benytter arrangementsystemet Checkin i forbindelse med enkelte arrangementer. Checkin er en
		tjeneste som tilbyr salg og betaling av billetter og organisering av arrangementer. Vi sletter
		opplysninger så snart arrangementet er avsluttet, men ikke senere enn 60 dager. Noen
		opplysninger kan være nødvendig å lagre lengre for å oppfylle regnskapslovens krav.
	</p>
	<p>
		Opplysningene lagres hos Checkin AS (og deres underleverandører) og av den grunn vår
		databehandler. Egen databehandleravtale er inngått. Informasjon er avgrenset til de
		arrangementer som er gjennomført ved bruk av Checkins system.
	</p>
	<p>
		Les mer om Checkins personvernerklæring her:
		<a href="https://www.checkin.no/personvernerklaering">
			https://www.checkin.no/personvernerklaering
		</a>
	</p>

	<h2>Hoopla</h2>

	<p>
		Vi benytter billettsystemet Hoopla i forbindelse med enkelte arrangementer. Hoopla er en
		tjeneste som tilbyr salg av billetter og organisering av arrangementer. Vi sletter opplysninger
		så snart arrangementet er avsluttet, men ikke senere enn 60 dager. Noen opplysninger kan være
		nødvendig å lagre lengre for å oppfylle regnskapslovens krav. Les mer om Hooplas
		personvernerklæring her: <a href="https://www.hoopla.no/arranger/personvern">
			https://www.hoopla.no/arranger/personvern</a
		>
	</p>

	<h2>Slack</h2>

	<p>
		Klynge Næringsforening benytter Slack for å skape et sted hvor medlemmer, gjester og andre
		interessert i foreningen å møtes digitalt. Når man registrerer seg på Slack hentes det inn
		opplysninger om navn, tidssone, bilde og arbeidssted og -tittel.
	</p>

	<p>
		Se også Slacks egen personvernerklæring:
		<a href="https://slack.com/trust/privacy/privacy-policy">
			https://slack.com/trust/privacy/privacy-policy
		</a>
	</p>

