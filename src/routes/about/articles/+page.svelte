<h1>Vedtekter for Klynge Næringsforening</h1>

<div>
	<article id="formaal-og-medlemskap">
		<h2><a href="#formaal-og-medlemskap">Formål og medlemskap</a></h2>
		<p>
			§1. Klynge Næringsforening er en næringsforening med et formål om å fremme medlemmenes
			interesse, samle mennesker med interesse forretning, bygge nettverk og hjelpe hverandre å
			lykkes. Foreningen baserer seg på frivillig engasjement og er partipolitisk og religiøst
			uavhengig.
		</p>
		<p>
			§2. Alle som har betalt kontigenten for inneværende medlemsår og godkjent av styret er regnet
			som medlemmer av Klynge Næringsforening.
		</p>
	</article>

	<article id="aarsmote">
		<h2><a href="#aarsmote">Årsmøtet</a></h2>

		<p>
			§3. Årsmøtet er foreningens øverste organ i foreningen, og skal holdes hvert medlemsår.
			Innkallingen skal sendes ut to uker i forveien. Alle medlemmer som har betalt
			medlemskontigenten minst to uker i forveien har tale-, forslags- og stemmerett. På årsmøtet
			skal alltid disse sakene bli tatt opp:
		</p>
		<ul>
			<li>1. Valg av møteleder og referent</li>
			<li>2. Årsmelding fra styret</li>
			<li>3. Vedtekstendringer</li>
			<li>4. Valg av leder og styremedlemmer</li>
		</ul>

		<p>
			§4. Det skal kalles inn til et ekstraordinært årsmøte når styret eller minst ⅓ av medlemmene
			krever det. Innkallingen skal sendes ut minst én uke i forveien, og årsmøtet kan bare gjøre
			vedtak i saker som er oppnevnt i innkallingen.
		</p>
	</article>

	<article id="styret">
		<h2><a href="#styret">Styret</a></h2>

		<p>
			§5. Styret er det øverste organet i foreningen mellom årsmøtet. Styret består av fem
			styremedlemmer valgt av årsmøtet, hvor én skal være leder. Styret sitter frem til neste
			årsmøte og har fullmakt til å ta inn nye styremedlemmer om noen slutter.
		</p>

		<p>
			§6. Styret kan bare gjøre vedtak når minst halvparten av medlemmene av styret er tilstede. Det
			skal holdes styremøter om leder eller minst to styremedlemmer krever det. Styret står
			solidarisk ansvarlig for foreningens økonomi, og kan gi andre personer disposisjons- og
			tegningsrett. Lederen av styret tegner for foreningen.
		</p>
	</article>

	<article id="diverse">
		<h2><a href="#diverse">Diverse</a></h2>

		<p>
			§7. Vedtektene er foreningens øverste regelverk, og kan bare endres ved ⅔ flertall på et
			årsmøte. Forslag til endringer må være sendt til styret senest én uke før et årsmøte, en
			endring i vedtektene trer ikke i kraft før etter årsmøtet endringene er vedtatt på er hevet,
			med mindre årsmøtet vedtar noe annet.
		</p>

		<p>
			§8. Alle vedtak i foreningens organer krever allment flertall om det ikke står noe annet i
			vedtektene. Ved like mange stemmer i styret eller på årsmøtet er det lederens stemme som
			avgjør saken. Medlemsår og regnskapsår skal følge kalenderåret.
		</p>

		<p>
			§9. Foreningen kan kun oppløses med ⅔ flertall på et ordinært årsmøte og et påfølgende
			ekstraordinært årsmøte hvor oppløsningen er den eneste saken som skal behandles. Ved
			oppløsning fastsetter årsmøtet fordelingen av foreningens verdier.
		</p>
	</article>
</div>

